#!/usr/bin/env bash
set -e

# Parameters
ADMIN_USER="${1:-admin}"
ADMIN_PASS="${2:-admin}"
SSL_ENABLED="${3:-true}"
LOG_LEVEL="${4:-info}"

info() { echo "[cups-config] $*"; }

info "Generating cupsd.conf with user=$ADMIN_USER, ssl=$SSL_ENABLED, log=$LOG_LEVEL"

# Convert log level to CUPS format
case "$LOG_LEVEL" in
    debug) CUPS_LOG="debug2" ;;
    info)  CUPS_LOG="info" ;;
    warn)  CUPS_LOG="warn" ;;
    error) CUPS_LOG="error" ;;
    *)     CUPS_LOG="info" ;;
esac

# Determine access control
ADMIN_ACCESS="Allow 127.0.0.1"

# Generate cupsd.conf
cat > /etc/cups/cupsd.conf << EOF
# Generated by cups-config.sh on $(date)
# CUPS Server Configuration

# Logging
LogLevel $CUPS_LOG
MaxLogSize 0
ErrorLog /var/log/cups/error_log
AccessLog /var/log/cups/access_log
PageLog /var/log/cups/page_log

# Listen on all interfaces, port 631
Listen 0.0.0.0:631
Listen /var/run/cups/cups.sock

# Browsing (printer discovery)
Browsing Off
BrowseLocalProtocols none

# Server settings
ServerName $(hostname)
ServerAlias *
ServerAdmin root@localhost
DefaultAuthType Basic
WebInterface Yes

# Encryption
DefaultEncryption IfRequested
SSLListen 0.0.0.0:631
ServerCertificate /etc/cups/ssl/server.crt
ServerKey /etc/cups/ssl/server.key

# Access restrictions
<Location />
  Order allow,deny
  Allow all
</Location>

<Location /admin>
  Order allow,deny
  $ADMIN_ACCESS
</Location>

<Location /admin/conf>
  AuthType Default
  Require user @SYSTEM
  Order allow,deny
  $ADMIN_ACCESS
</Location>

<Location /admin/log>
  AuthType Default
  Require user @SYSTEM
  Order allow,deny
  $ADMIN_ACCESS
</Location>

<Policy default>
  JobPrivateAccess default
  JobPrivateValues default
  SubscriptionPrivateAccess default
  SubscriptionPrivateValues default
  
  <Limit All>
    Order deny,allow
  </Limit>
  
  <Limit Send-Document Send-URI Hold-Job Release-Job Restart-Job Purge-Jobs Set-Job-Attributes Create-Job-Subscription Renew-Subscription Cancel-Subscription Get-Notifications Reprocess-Job Cancel-Current-Job Suspend-Current-Job Resume-Job Cancel-My-Jobs Close-Job CUPS-Move-Job CUPS-Get-Document>
    Require user @OWNER @SYSTEM
    Order deny,allow
  </Limit>
  
  <Limit CUPS-Add-Modify-Printer CUPS-Delete-Printer CUPS-Add-Modify-Class CUPS-Delete-Class CUPS-Set-Default CUPS-Get-Devices>
    AuthType Default
    Require user @SYSTEM
    Order deny,allow
  </Limit>
  
  <Limit Pause-Printer Resume-Printer Enable-Printer Disable-Printer Pause-Printer-After-Current-Job Hold-New-Jobs Release-Held-New-Jobs Deactivate-Printer Activate-Printer Restart-Printer Shutdown-Printer Startup-Printer Promote-Job Schedule-Job-After Cancel-Jobs CUPS-Accept-Jobs CUPS-Reject-Jobs>
    AuthType Default
    Require user @SYSTEM
    Order deny,allow
  </Limit>
  
  <Limit Cancel-Job CUPS-Authenticate-Job>
    Require user @OWNER @SYSTEM
    Order deny,allow
  </Limit>
  
  <Limit All>
    Order deny,allow
  </Limit>
</Policy>

<Policy authenticated>
  JobPrivateAccess default
  JobPrivateValues default
  SubscriptionPrivateAccess default
  SubscriptionPrivateValues default
  
  <Limit All>
    AuthType Default
    Order deny,allow
  </Limit>
</Policy>
EOF

# Create CUPS admin user
info "Creating/updating CUPS admin user: $ADMIN_USER"

# Create system user if it doesn't exist
if ! id "$ADMIN_USER" &>/dev/null; then
    useradd -r -G lpadmin -s /bin/bash "$ADMIN_USER"
fi

# Ensure user is in lpadmin group
usermod -a -G lpadmin "$ADMIN_USER" 2>/dev/null || true

# Set password
echo "$ADMIN_USER:$ADMIN_PASS" | chpasswd

# Fix permissions
chmod 640 /etc/cups/cupsd.conf
chown root:lp /etc/cups/cupsd.conf

info "cupsd.conf generated successfully"
